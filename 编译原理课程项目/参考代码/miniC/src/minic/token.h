#pragma once
#include "common.h"

/*******************************************************************************
                                   词法记号
*******************************************************************************/

class Token
{
public:
	Tag tag;//内部标签
	Token (Tag t);
	virtual string toString();
	virtual ~Token ();
};


/*******************************************************************************
                                   标识符
*******************************************************************************/

class Id:public Token
{
public:
	string name;
	Id (string n);
	virtual string toString();
};


/*******************************************************************************
                                   字符串
*******************************************************************************/

class Str:public Token
{
public:
	string str;
	Str (string s);
	virtual string toString();
};


/*******************************************************************************
                                   数字
*******************************************************************************/

class Num:public Token
{
public:
	int val;
	Num (int v);
	virtual string toString();
};


/*******************************************************************************
                                   字符
*******************************************************************************/

class Char:public Token
{
public:
	char ch;
	Char (char c);
	virtual string toString();
};
